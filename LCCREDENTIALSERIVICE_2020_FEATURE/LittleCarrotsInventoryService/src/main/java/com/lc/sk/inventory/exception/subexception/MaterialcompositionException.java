package com.lc.sk.inventory.exception.subexception;

public class MaterialcompositionException extends RuntimeException {

	private static final long serialVersionUID = 6233573703921361736L;

	public MaterialcompositionException() {
		super();
	}

	public MaterialcompositionException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public MaterialcompositionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MaterialcompositionException(String arg0) {
		super(arg0);
	}

	public MaterialcompositionException(Throwable arg0) {
		super(arg0);
	}

}
